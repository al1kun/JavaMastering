public class lesson3_EnglishTask {
    public static void main(String[] args) {
        int days = 30;
        int episodeNumber = 3;
        for (int i = 1; i <= days; i++) {
            System.out.println("Day" + i);
            for (int j = 1; j <= episodeNumber; j++) {
                System.out.println(" Episode: " + j);
            }
        }
        System.out.println("Awesome! You sharpened your Java, and english language skills!");
    }
}
